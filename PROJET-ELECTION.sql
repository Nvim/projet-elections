use LEGISLATIVES; 

create table if not exists Election(
	ID_scrutin INTEGER PRIMARY KEY NOT NULL, 
	LIBELLE_scrutin VARCHAR(300),	
    DATE_scrutin DATE
); 


create table if not exists Inscription(
	ID_inscription VARCHAR(10) PRIMARY KEY NOT NULL, 
    Ref_election INT, 
    Ref_bureau INT, 
    nbinscrits INT, 
    nbvotants INT, 
    nbexprimes INT
); 

ALTER TABLE INSCRIPTION add constraint FOREIGN KEY(Ref_election) REFERENCES Election(ID_scrutin);
ALTER TABLE INSCRIPTION add constraint foreign key(Ref_bureau) references BUREAU(num_bureau); 


create table  Candidat(
	numdepot INTEGER PRIMARY KEY NOT NULL, 
    Nom VARCHAR(300), 
    Prenom VARCHAR(300)
); 


create table if not exists VOTES(
	Ref_Election INT,
    Ref_Bureau INT, 
    Ref_Candidat INT,  
    nb_votes INT,
    constraint pk_v PRIMARY KEY (Ref_Election, Ref_Bureau, Ref_Candidat), 
    constraint pf_v1  foreign key(Ref_candidat) references candidat(numdepot),
    constraint pf_v2  foreign key(Ref_election) references election(id_scrutin),
    constraint pf_v3 foreign key(Ref_bureau) references bureau(num_bureau)
); 

create table if not exists BUREAU(
	Num_Bureau INT PRIMARY KEY, 
    CP INT
); 

